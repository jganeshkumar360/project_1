package com.green;

public class Switch {

	public static void main(String[] args) {
		
		int month = 1;
		switch  (month){
		
		
		case 1 :
			System.out.println("condition 1");
			
		case 2 :
			System.out.println("condition 2");
			
		case 3 :
			System.out.println("condition 3");
			
		case 4 :
			System.out.println("condition 4");
			break;
		case 5 :
			System.out.println("condition 5");
			
		}
		
	
		
		
	}
	
	
}
